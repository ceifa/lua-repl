const library = (documentation, members) => ({
    documentation,
    members,
})

const fn = (signature, documentation, extra = {}) => ({
    kind: 'function',
    signature,
    documentation,
    readonly: true,
    ...extra,
})

const value = (kind, documentation, extra = {}) => ({
    kind,
    documentation,
    readonly: true,
    ...extra,
})

